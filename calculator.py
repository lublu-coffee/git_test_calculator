def plus(a, b):
    print(f'Сумма: {a} + {b} = ', a + b)


def minus(a, b):
    print(f'Разность: {a} - {b} = ', a - b)


def multiplication(a, b):
    print(f'Умножение: {a} * {b} = ', a * b)


def deletion(a, b):
    print(f'Умножение: {a} / {b} = ', a / b)


# плюс
plus(2, 3)
plus(4, 5)

# минус
minus(3, 1)
minus(4, 2)

# умножение
multiplication(5, 1)
multiplication(4, 7)


# деление
deletion(3, 1)
deletion(4, 2)